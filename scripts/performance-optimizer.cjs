#!/usr/bin/env node
/**
 * Performance Optimizer Script
 * Addresses Lighthouse performance issues:
 * - Optimizes images
 * - Generates responsive image sizes
 * - Updates cache headers
 * - Implements critical CSS inlining
 * - Adds resource hints
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

console.log('🚀 Performance Optimization Starting...\n');

// ============================================================================
// 1. OPTIMIZE LOGO IMAGE (40KB → ~5KB for 77x77 display)
// ============================================================================
console.log('📸 Step 1: Optimizing logo image...');

const logoPath = path.join(__dirname, '../public/assets/img/logo-CczQ7lzO.webp');
const logoOptimizedPath = path.join(__dirname, '../public/assets/img/logo-optimized.webp');

if (fs.existsSync(logoPath)) {
  try {
    // Check if sharp is available
    try {
      const sharp = require('sharp');
      
      sharp(logoPath)
        .resize(77, 77, { fit: 'contain', background: { r: 0, g: 0, b: 0, alpha: 0 } })
        .webp({ quality: 85 })
        .toFile(logoOptimizedPath)
        .then(() => {
          const originalSize = fs.statSync(logoPath).size;
          const optimizedSize = fs.statSync(logoOptimizedPath).size;
          const savings = ((1 - optimizedSize / originalSize) * 100).toFixed(1);
          console.log(`   ✅ Logo optimized: ${(originalSize/1024).toFixed(1)}KB → ${(optimizedSize/1024).toFixed(1)}KB (${savings}% reduction)`);
        });
    } catch (e) {
      console.log('   ⚠️  Sharp not installed. Install with: npm install sharp');
      console.log('   💡 Manual fix: Resize logo to 77x77px and re-export at 85% quality');
    }
  } catch (error) {
    console.log(`   ⚠️  Could not optimize logo: ${error.message}`);
  }
} else {
  console.log('   ℹ️  Logo file not found at expected path');
}

